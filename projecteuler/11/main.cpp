int max_product_diag(const int chunk[][20], int offset_i, int offset_j){
    int nwse = 1;
    int nesw = 1;
    for (int i = 0; i < 4; i++) nwse*=chunk[offset_i+i][offset_j+i];
    for (int i = 0; i < 4; i++) nesw*=chunk[offset_i+i][offset_j+3-i];
    return (nwse>nesw?nwse:nesw);
}
